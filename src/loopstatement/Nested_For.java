package loopstatement;

public class Nested_For {

	public static void main(String[] args) {
		// Nested for statement
		int i;
		int j;
		for(i=1;i<=5;i++) {
			for(j=1;j<=i;j++) {
				System.out.print (j);
				
			}System.out.println ();
			                       
			
		}

	}

}
