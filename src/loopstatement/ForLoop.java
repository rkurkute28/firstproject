package loopstatement;

public class ForLoop {

	public static void main(String[] args) {
		// Standard Syntax of for
		// for (Declaration+initialization, Condition, Pre/Post Increment /decrement)
		//{Block of code}
		//Print the number from 11 to 20
	
		int b ;
		for (b=110;b>99;b--) {
			System.out.println(b);
		}
		int a ;
		for (a=100;a<=150;a++) {
			System.out.println(a);
		}
		int i;
		for(i=3; i<=5;i++);
		System.out.println(i);
	}

}
